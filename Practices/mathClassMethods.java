public class mathClassMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-8.0)); // absolute value
        System.out.println(Math.ceil(7.3)); // rounds up
        System.out.println(Math.floor(8.8)); // rounds down
        System.out.println(Math.pow(2,3)); // 2 is the base and 3 is the power
        System.out.println(Math.max(8,6.1)); // gives the maximum between 2 numbers seperated by a comma 
        System.out.println(Math.min(8,9.7)); // gives the minimum between 2 numbers seperated by a comma
        System.out.println(Math.sqrt(64));
        System.out.println(Math.log(1)); //to find ln(1)
        System.out.println(Math.log10(100)); //to find log(10 base) 100
        System.out.println(Math.log(81)/Math.log(3)); //to find log(3 base) 81
    }
}